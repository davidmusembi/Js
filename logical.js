// //Using AND operator && operator
// //In AND operation if both condition are true you get true
let x=7 ,y=8 ,z=5 ;
console.log(x<y && x<z);

// //Using OR operator || operator
// //In OR operation if one of the condition is true you get true
console.log(x<y || x>z);

// //Using NOT operator ! operator
// //In NOT operator if one of the condition is false you get true
console.log(!(x>y));

