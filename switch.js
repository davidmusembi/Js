 //Using switch case for the user input
 let day =prompt("Enter a day");
    switch(day){
            case "monday":
            console.log("7am");
            break;
            case "Tuesday":
            console.log("8am");
            break;
            case "Wednesday":
            console.log("9am");
            break;
            case "Thursday":
            console.log("10am");
            break;
            case "Friday":
            console.log("11am");
            break;
            case "Saturday":
            console.log("12pm");
            break;
            case "Sunday":
            console.log("1pm");
            break;
            default:

    }